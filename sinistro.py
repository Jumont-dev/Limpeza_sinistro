def remover_colunas(df):
  coluna_remover = ["ano_sinistro", "mes_sinistro", "dia_sinistro", "ano_mes_sinistro", "dia_da_semana", "turno", "tipo_local", "cod_ibge", "regiao_administrativa", "administracao", "conservacao", "circunscricao", "qtd_gravidade_ileso", "tp_sinistro_atropelamento", "tp_sinistro_colisao_frontal", "tp_sinistro_colisao_traseira", "tp_sinistro_colisao_lateral", "tp_sinistro_colisao_transversal", "tp_sinistro_colisao_outros", "tp_sinistro_choque", "tp_sinistro_capotamento", "tp_sinistro_engavetamento", "tp_sinistro_tombamento", "tp_sinistro_outros", "tp_sinistro_nao_disponivel"]
  df = df.drop(columns=coluna_remover, errors='ignore')
  return df


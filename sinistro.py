if 'hora_sinistro' in df.columns:
    def convert_to_time_robust(value):
        if pd.isna(value):
            return pd.NaT
        if isinstance(value, datetime.time):
            return value
        try:

            return pd.to_datetime(str(value)).time()
        except:

            return pd.NaT

    original_nan_count_hs = df['hora_sinistro'].isnull().sum()
    df['hora_sinistro'] = df['hora_sinistro'].apply(convert_to_time_robust)
    new_nan_count_hs = df['hora_sinistro'].isnull().sum()

    print(f"Coluna 'hora_sinistro' convertida para o tipo de tempo. NaNs originais: {original_nan_count_hs}, NaNs após conversão: {new_nan_count_hs}.")
